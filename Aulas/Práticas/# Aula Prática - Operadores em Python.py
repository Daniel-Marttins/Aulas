# Aula Prática - Operadores em Python

# Nesta atividade, vamos explorar os principais operadores em Python
# e entender como utilizá-los na prática.

# 1. Operadores Aritméticos (Matemáticos)
# Utilizados para realizar operações matemáticas básicas.

print('Operadores Aritméticos')

numero1 = 12
numero2 = 4

# Soma
soma = numero1 + numero2
print(f"Soma: {numero1} + {numero2} = {soma}")

# Subtração
subtracao = numero1 - numero2
print(f"Subtração: {numero1} - {numero2} = {subtracao}")

# Multiplicação
multiplicacao = numero1 * numero2
print(f"Multiplicação: {numero1} * {numero2} = {multiplicacao}")

# Divisão
divisao = numero1 / numero2
print(f"Divisão: {numero1} / {numero2} = {divisao}")

# Divisão inteira
divisao_inteira = numero1 // numero2
print(f"Divisão inteira: {numero1} // {numero2} = {divisao_inteira}")

# Módulo (resto da divisão)
modulo = numero1 % numero2
print(f"Módulo: {numero1} % {numero2} = {modulo}")

# Potência
potencia = numero1 ** numero2
print(f"Potência: {numero1} ** {numero2} = {potencia}")

print('\n')

# 2. Operadores de Comparação
# Usados para comparar valores e retornam True ou False.

print('Operadores de Comparação')

print(f"Igual: {numero1} == {numero2} -> {numero1 == numero2}")
print(f"Diferente: {numero1} != {numero2} -> {numero1 != numero2}")
print(f"Maior que: {numero1} > {numero2} -> {numero1 > numero2}")
print(f"Menor que: {numero1} < {numero2} -> {numero1 < numero2}")
print(f"Maior ou igual: {numero1} >= {numero2} -> {numero1 >= numero2}")
print(f"Menor ou igual: {numero1} <= {numero2} -> {numero1 <= numero2}")

print('\n')

# 3. Operadores Lógicos
# Usados para realizar operações lógicas entre expressões booleanas.

print('Operadores Lógicos')

condicao1 = True
condicao2 = False

print(f"AND (E): {condicao1} and {condicao2} -> {condicao1 and condicao2}")
print(f"OR (OU): {condicao1} or {condicao2} -> {condicao1 or condicao2}")
print(f"NOT (NÃO): not {condicao1} -> {not condicao1}")

print('\n')

# 4. Operadores de Atribuição
# Usados para simplificar operações matemáticas com variáveis.

print('Operadores de Atribuição')

numero3 = 20
print(f"Atribuição simples: numero3 = {numero3}")

numero3 += 5
print(f"Atribuição com soma: numero3 += 5 -> {numero3}")

numero3 *= 2
print(f"Atribuição com multiplicação: numero3 *= 2 -> {numero3}")

print('\n')

