# Aula Prática - Trabalhando com Variáveis em Python

# Nesta atividade, vamos aprender a declarar e manipular variáveis.
# As variáveis armazenam informações que podemos reutilizar ao longo do programa.

# Passo 1: Declarando variáveis
# Escolha dois nomes para representar uma pessoa e atribua valores a eles
primeiro_nome = 'John'
sobrenome = 'Doe'

# Passo 2: Exibindo os valores armazenados nas variáveis
# Utilizamos a função print para exibir as informações na tela
print(f"Nome Completo: {primeiro_nome} {sobrenome}")

